const express = require('express')

const app = express()
app.use(express.json())

const notes = []

/*post method*/
app.post("/notes",(req,res)=>{
     console.log(req.body);
     notes.push(req.body)
    res.send("note created successful")
})

/*get method*/
app.get("/notes",(req,res)=>{
    res.send(notes)
})


app.listen(3000,()=>{
    console.log("server running on port 3000");
    
})